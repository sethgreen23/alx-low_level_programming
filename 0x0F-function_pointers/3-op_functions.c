#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - add two values
 * @a: value one
 * @b: value two
 * Return: return the result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub two values
 * @a: value one
 * @b: value two
 * Return: return the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two values
 * @a: value one
 * @b: value two
 * Return: return the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two values
 * @a: value one
 * @b: value two
 * Return: return the result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo two values
 * @a: value one
 * @b: value two
 * Return: return the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
