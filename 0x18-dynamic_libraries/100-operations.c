#include <stdio.h>
/**
 * add - add to values
 * @a: a
 * @b: b
 *
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sub of  two values
 * @a: a
 * @b: b
 *
 * Return: sub of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - mul of  two values
 * @a: a
 * @b: b
 *
 * Return: multiplication of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - mod of  two values
 * @a: a
 * @b: b
 *
 * Return: mod of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * div - division of  two values
 * @a: a
 * @b: b
 *
 * Return: mod of a and b
 */
int div(int a, int b)
{
	return (a / b);
}
